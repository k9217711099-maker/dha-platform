const h2 = (text: string): KbBlock => ({ type: 'heading', level: 2, text });
const h3 = (text: string): KbBlock => ({ type: 'heading', level: 3, text });
const p = (html: string): KbBlock => ({ type: 'text', html: `<p>${html}</p>` });
const ul = (items: string[]): KbBlock => ({ type: 'text', html: `<ul>${items.map((i) => `<li>${i}</li>`).join('')}</ul>` });
const ol = (items: string[]): KbBlock => ({ type: 'text', html: `<ol>${items.map((i) => `<li>${i}</li>`).join('')}</ol>` });
const note = (html: string): KbBlock => ({ type: 'text', html: `<p>📌 <strong>Важно.</strong> ${html}</p>` });
const divider = (): KbBlock => ({ type: 'divider' });
const table = (head: string[], rows: string[][]): KbBlock => ({
  type: 'text',
  html:
    `<table><tbody><tr>${head.map((hh) => `<td><strong>${hh}</strong></td>`).join('')}</tr>` +
    rows.map((r) => `<tr>${r.map((c) => `<td>${c}</td>`).join('')}</tr>`).join('') +
    `</tbody></table>`,
});
const owner = (role: string): KbBlock => ({ type: 'text', html: `<p><em>Ответственный: ${role} · Обновлено: при внедрении.</em></p>` });

