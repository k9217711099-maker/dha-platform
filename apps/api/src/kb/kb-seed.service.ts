@Injectable()
export class KbSeedService implements OnModuleInit {
  private readonly logger = new Logger('KbSeed');

  constructor(
    private readonly prisma: PrismaService,
    private readonly settings: SettingsService,
    private readonly tenant: TenantService,
    private readonly kb: KbService,
  ) {}

  async onModuleInit(): Promise<void> {
    try {
      if (await this.settings.get(SEED_FLAG)) return; // уже засеяно
      const tenantId = await this.tenant.getDefaultTenantId();
      // Дополнительная защита от дублей: если база с таким именем уже есть — считаем засеяно.
      const exists = await this.prisma.kbBase.findFirst({ where: { tenantId, name: BASE_NAME }, select: { id: true } });
      if (exists) {
        await this.settings.set(SEED_FLAG, '1');
        return;
      }
      await this.seed(tenantId);
      await this.settings.set(SEED_FLAG, '1');
      this.logger.log('База знаний засеяна каркасом (#5).');
    } catch (e) {
      this.logger.warn(`KB seed пропущен: ${(e as Error).message}`);
    }
  }

  private async seed(tenantId: string): Promise<void> {
    const base = await this.kb.createBase(tenantId, { name: BASE_NAME, icon: '📚' });
    let order = 0;
    // Стандарт оформления — первым.
    await this.createTree(tenantId, base.id, null, STANDARD, order++);
    for (const section of SECTIONS) {
      await this.createTree(tenantId, base.id, null, section, order++);
    }
  }

  private async createTree(
    tenantId: string,
    baseId: string,
    parentId: string | null,
    node: SeedPage,
    sortOrder: number,
  ): Promise<void> {
    const page = await this.kb.createPage(tenantId, {
      baseId,
      parentId,
      title: node.title,
      icon: node.icon ?? null,
      content: { blocks: node.blocks },
      status: KbPageStatus.PUBLISHED,
      sortOrder,
    });
    if (node.children?.length) {
      let childOrder = 0;
      for (const child of node.children) {
        await this.createTree(tenantId, baseId, page.id, child, childOrder++);
      }
    }
  }
}
