/** Всплывающий выбор эмодзи: категории + сетка. */
export function EmojiPicker({ onPick, onClose, className = '' }: { onPick: (emoji: string) => void; onClose: () => void; className?: string }) {
  const [cat, setCat] = useState(EMOJI_CATEGORIES[0]!.key);
  const ref = useRef<HTMLDivElement>(null);

  // Сдвигаем пикер если он выходит за границы экрана (запускается до paint — без мерцания)
  useLayoutEffect(() => {
    const el = ref.current;
    if (!el) return;
    const r = el.getBoundingClientRect();
    const dy = Math.min(0, window.innerHeight - 8 - r.bottom);
    const dx = Math.min(0, window.innerWidth - 8 - r.right);
    if (dy !== 0 || dx !== 0) el.style.transform = `translate(${dx}px,${dy}px)`;
  }, []);

  const active = EMOJI_CATEGORIES.find((c) => c.key === cat) ?? EMOJI_CATEGORIES[0]!;
  return (
    <>
      <div className="fixed inset-0 z-30" onClick={onClose} />
      <div ref={ref} className={`z-40 w-80 rounded-xl border border-ink/10 bg-white shadow-2xl ${className}`}>
        <div className="flex flex-wrap items-center gap-0.5 border-b border-ink/[0.06] px-2 py-1.5">
          {EMOJI_CATEGORIES.map((c) => (
            <button key={c.key} type="button" title={c.title} onClick={() => setCat(c.key)}
              className={`rounded-md px-1.5 py-0.5 text-base transition ${cat === c.key ? 'bg-primary-50' : 'opacity-60 hover:opacity-100'}`}>
              {c.icon}
            </button>
          ))}
        </div>
        <div className="max-h-56 overflow-y-auto p-2">
          <p className="mb-1 px-1 text-[10px] font-medium uppercase tracking-wide text-slate-400">{active.title}</p>
          {/* inline style — гарантирует 8 колонок независимо от Tailwind JIT-кэша */}
          <div style={{ display: 'grid', gridTemplateColumns: 'repeat(8, minmax(0, 1fr))', gap: '2px' }}>
            {active.emojis.map((e) => (
              <button key={e} type="button" onClick={() => onPick(e)} className="rounded-md p-1 text-lg leading-none transition hover:bg-slate-100">
                {e}
              </button>
            ))}
          </div>
        </div>
      </div>
    </>
  );
}
